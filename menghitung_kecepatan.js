// Jarak yang akan ditempuh dalam kilometer
const distance = 200;

// Kecepatan rata-rata dalam kilometer per jam
const averageSpeed = 60;

// Menghitung waktu yang dibutuhkan dalam jam
const time = distance / averageSpeed;

console.log(`Waktu yang dibutuhkan untuk menempuh jarak ${distance} km dengan kecepatan rata-rata ${averageSpeed} km/jam adalah ${time} jam.`);
